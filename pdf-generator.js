const { PDFDocument, StandardFonts, rgb } = require('pdf-lib');

function formatDate(val) {
  if (!val) return '';
  const d = new Date(val);
  if (isNaN(d.getTime())) return String(val);
  return d.toLocaleDateString('en-US', { year: 'numeric', month: 'short', day: '2-digit' });
}

async function createPdf(data) {
  const doc = await PDFDocument.create();
  const font = await doc.embedFont(StandardFonts.Helvetica);
  const fontBold = await doc.embedFont(StandardFonts.HelveticaBold);
  const page = doc.addPage([612, 792]);
  const { width, height } = page.getSize();
  const margin = 50;
  let y = height - margin;

  const title = 'PURCHASE ORDER';
  page.drawText(title, {
    x: margin,
    y,
    size: 18,
    font: fontBold,
    color: rgb(0.1, 0.2, 0.4),
  });
  y -= 28;

  page.drawText('Spiro Medical', {
    x: margin,
    y,
    size: 12,
    font: fontBold,
    color: rgb(0.2, 0.2, 0.3),
  });
  y -= 20;

  const line = (label, value) => {
    if (value === undefined || value === null) value = '';
    const str = String(value).trim();
    page.drawText(`${label}:`, { x: margin, y, size: 10, font: fontBold, color: rgb(0.2, 0.2, 0.3) });
    page.drawText(str, { x: margin + 120, y, size: 10, font, color: rgb(0.1, 0.1, 0.2) });
    y -= 18;
  };

  line('PO #', data.poNumber);
  line('Quote #', data.quoteNumber);
  line('Date', formatDate(data.date));
  line('Placed by', data.placedBy);
  line('For', data.for);
  line('Vendor / Supplier', data.vendor || '');
  line('Total Cost', data.totalCost != null ? `$${Number(data.totalCost).toFixed(2)}` : '');
  y -= 10;

  if (data.description && String(data.description).trim()) {
    page.drawText('Description / Notes:', { x: margin, y, size: 10, font: fontBold, color: rgb(0.2, 0.2, 0.3) });
    y -= 16;
    const desc = String(data.description).trim();
    const maxWidth = width - 2 * margin;
    const lines = desc.split(/\r?\n/).filter(Boolean);
    for (const ln of lines) {
      if (y < margin + 40) break;
      page.drawText(ln.substring(0, 90), { x: margin, y, size: 10, font, color: rgb(0.15, 0.15, 0.25) });
      y -= 14;
    }
    y -= 8;
  }

  y -= 20;
  page.drawText('— Generated by Spiro Medical PO Generator. All data stays on your device. —', {
    x: margin,
    y,
    size: 8,
    font,
    color: rgb(0.5, 0.5, 0.55),
  });

  return doc.save();
}

module.exports = { createPdf };
